export function getInsightBySlug(slug: string, lang: "en" | "lt" = "en"): InsightArticle | undefined {
  const article = INSIGHTS.find((a) => a.slug === slug);
  if (!article || lang === "en") return article;

  // Lazy-load LT translations
  try {
    // eslint-disable-next-line @typescript-eslint/no-require-imports
    const { INSIGHTS_LT } = require("./insights-lt");
    const lt = INSIGHTS_LT[slug];
    if (lt) {
      return {
        ...article,
        title: lt.title,
        category: lt.category,
        excerpt: lt.excerpt,
        content: lt.content,
      };
    }
  } catch {
    // LT file not available yet
  }
  return article;
}

